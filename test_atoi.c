#include "header.h"

void	test_atoi(int *score)
{
	int	i = 0;
	char *arr[] = {MAX_INT, MIN_INT, "1234", "-1234", "0000", "   1234", "    -123", "\t\t\n123", "\t-\t123", "-\t123", "", "+123", "  +123", "--+123", "  --123"};
	for (int j = 0; j < 15; j++)
	{
		atoi(arr[j]) == ft_atoi(arr[j]) ? correct(score, ++i) : wrong(++i);
		usleep(100 * MILISECONDE);
	}
}
